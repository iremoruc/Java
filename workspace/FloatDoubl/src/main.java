
public class main {
    public static void main(String[] args) {
   
      /* double= 64 bit - 8 byte
         float = 32 bit - 4 byte
         dönüştürm = int --> float --> double
        */
       
        
       double a = 3.15;
       double b = 5.0;
       double c = 4d; // çıktısı 4.0 şeklinde//
       double d = 4.23d; // çıktısı 4.23 //
       
        System.out.println(a + b + c + d);
       float x = (float) 5.0;
       float y = 6.2f;
       float z = 5f;
        System.out.println(x + y + z);
       
       int j = 22 / 7;
       float k = 22f / 7f;
       double l = 22d / 7d;
       
       
        System.out.println("j:" + j);
        System.out.println("k:" + k);
        System.out.println("l:" + l);
        
       
        
                
        
        
        
}
    
    
    
       
    }

