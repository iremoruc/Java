
public class main {
    public static void main(String[] args) {
        
        /*      
        mantıksal operatörler
        
        && --> and operatörü
        Bütün sonuçlar kendi içinde true ise sonuç true,
        en az birisi bile false ise sonuç false olur.
        
        || --> or operatörü
        Sonuçlardan en az birisi bile true ise sonuç true çıkar,
        hepsi false ise sonuç false çıkar.
        
        ! --> not oparatörü
        */
        
        System.out.println( 4 == 4);
        System.out.println( 2 < 3);
        System.out.println( 4 == 4 && 2 < 3);
        System.out.println( 4 == 3 || 2 < 3);
       
        
        
        
        
        
        
    }
    
}
